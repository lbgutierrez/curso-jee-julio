package cl.ibm.residencia.vo;

public class ResidenteVO {

	private String rut;
	private String nombre;
	private String isResidente;
	
	public String getRut() {
		return rut;
	}
	public void setRut(String rut) {
		this.rut = rut;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getIsResidente() {
		return isResidente;
	}
	public void setIsResidente(String isResidente) {
		this.isResidente = isResidente;
	}
	
	
	
}
